package u128

import (
	"github.com/minio/highwayhash"
)

var key = [32]uint8{0xd, 0x1a, 0xca, 0xb5, 0x6d, 0x16, 0xc7, 0xb8, 0x65, 0x51, 0x65, 0xea, 0x81, 0x92, 0x97, 0xeb, 0xc6, 0xe8, 0x72, 0x9e, 0x90, 0x68, 0x9a, 0xf8, 0x14, 0x8b, 0xdb, 0x3f, 0x6a, 0x15, 0x75, 0x9f}

type U128 = [16]byte

// Hash returns 128 bit hash.
func Hash(data []byte) U128 {
	return highwayhash.Sum128(data, key[:])
}
